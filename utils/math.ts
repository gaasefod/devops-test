const sum = (int1: number, int2: number) => int1 + int2;

const subtract = (int1: number, int2: number) => int1 - int2;

const divide = (int1: number, int2: number) => int1 / int2;

const multiply = (int1: number, int2: number) => int1 * int2;

const modulo = (int1: number, int2: number) => int1 % int2;

export { sum, subtract, divide, multiply, modulo };
